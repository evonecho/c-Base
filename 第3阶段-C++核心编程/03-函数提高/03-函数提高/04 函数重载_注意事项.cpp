#include<iostream>
using namespace std;

/*
函数重载的注意事项：
1、引用作为重载的条件
*/
void fun(int &a)	//int &a = 10;不合法
{
	cout << "func(int &a)调用。a=" << a << endl;
}

void fun(const int &a)	//const int &a = 10;合法
{
	cout << "func(const int &a)调用。a=" << a << endl;
}

//2、函数重载碰到默认参数
void func2(int a, int b = 40)
{
	cout << "fun2(int a)的调用" << endl;
}
void func2(int a)
{
	cout << "fun2(int a)的调用" << endl;
}

int main()
{
	int a = 10;
	fun(a);

	fun(20);//传入常量20

	const int b = 15;
	fun(b);

	//func2(30);//当函数重载碰到默认参数，出现二义性，报错，尽量避免这种情况

	system("pause");
	return 0;
}
